package Map;

public class BuffItem {
    public int speed = 1;
    public int sizeBom = 0;
    public int bomb = 1;

    public BuffItem(){

    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getSizeBom() {
        return sizeBom;
    }

    public void setSizeBom(int sizeBom) {
        this.sizeBom = sizeBom;
    }

    public int getBomb() {
        return bomb;
    }

    public void setBomb(int bomb) {
        this.bomb = bomb;
    }
}
